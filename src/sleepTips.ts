export const SLEEP_TIPS: SleepTip[] = Array.from({ length: 365 }, (_, index) => {
  const theme = themes[index % themes.length];
  const action = actions[index % actions.length];

  return {
    id: index + 1,
    title: `${theme}の豆知識 ${index + 1}`,
    body: `${action}今日は「${theme}」を意識して、無理なく続く小さな調整を試してみましょう。`,
  };
});

export function getDailyTip(date = new Date()) {
  const start = new Date(date.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((date.getTime() - start.getTime()) / 86400000);
  const index = Math.max(0, (dayOfYear - 1) % SLEEP_TIPS.length);

  return {
    ...SLEEP_TIPS[index],
    dayLabel: `${weekLabel(index)} / ${date.toLocaleDateString('ja-JP')}`,
  };
}

function weekLabel(index: number) {
  return `第${Math.floor(index / 7) + 1}週`;
}
